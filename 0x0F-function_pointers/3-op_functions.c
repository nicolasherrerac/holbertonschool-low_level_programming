#include "3-calc.h"
/**
* op_add - sum
* @a: num 1
* @b: num 2
* Return: sum
*/
int op_add(int a, int b)
{
	return (a +  b);
}
/**
* op_sub - subtract
* @a: num 1
* @b: num 2
* Return: subtract
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
* op_mul - multiplication
* @a: num 1
* @b: num 2
*Return: multiplication
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
* op_div - division
* @a: num 1
* @b: num 2
* Return: division
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
* op_mod - module of div
* @a: num 1
* @b: num 2
* Return: module
*/
int op_mod(int a, int b)
{
	return (a % b);
}
